# Input A Name In Python
name=input('ENTER A NAME ')

#strip is a string method that removes all white spaces
name=name.strip()
print(f"The name is {name}")


#capitalize just makes the first letter of the first word capital
name=name.capitalize()
print(f"The name is {name}")

#title is another string method that makes the first letter of each word capital
name=name.title()

# Showcasing the value of the variable in the print function
print(f"The name is {name}")

